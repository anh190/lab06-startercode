def first(collection):
    ''' first item of a string, list, tuple, or other indexable thing '''
    return collection[0]

def rest(collection):
    ''' all but the first item of a indexable thing (str, list, tuple) '''
    return collection[1:]

def last(collection):
    ''' last item of an indexable thing '''
    return "stub"

def allButLast(collection):
    ''' all but the last item of an indexable thing '''
    return "stub"
